#!/usr/bin/env node
/**
 * Generates global admin permission artifacts from base menu.json.
 * This script owns source parsing and code generation only.
 * It does not import menus into the database or mutate runtime auth state.
 * Keep output ordering stable; bit drift here will break permissionMask semantics.
 */

import fs from 'node:fs';
import path from 'node:path';
import process from 'node:process';
import { fileURLToPath } from 'node:url';
import {
	collectPermissionModel,
	loadBasePermissionSourceConfig
} from '../cool-admin-midway/src/modules/base/domain/permissions/source.mjs';

const SCRIPT_DIR = path.dirname(fileURLToPath(import.meta.url));
const REPO_ROOT = path.resolve(SCRIPT_DIR, '..');
const BASE_PERMISSION_SOURCE_CONFIG = loadBasePermissionSourceConfig(REPO_ROOT);
const MENU_SOURCE_PATH = path.join(
	REPO_ROOT,
	BASE_PERMISSION_SOURCE_CONFIG.menuSourcePath
);

const OUTPUT_TARGETS = BASE_PERMISSION_SOURCE_CONFIG.generatedTargets.map(
	targetPath => path.join(REPO_ROOT, targetPath)
);

function toCamelCase(value) {
	const normalized = String(value || '')
		.replace(/[^a-zA-Z0-9]+([a-zA-Z0-9])/g, (_, char) => char.toUpperCase())
		.replace(/[^a-zA-Z0-9]/g, '');

	if (!normalized) {
		return '';
	}

	return normalized.slice(0, 1).toLowerCase() + normalized.slice(1);
}

function toPascalCase(value) {
	const camelValue = toCamelCase(value);
	if (!camelValue) {
		return '';
	}
	return camelValue.slice(0, 1).toUpperCase() + camelValue.slice(1);
}

function formatTsObjectKey(key) {
	return /^[A-Za-z_$][A-Za-z0-9_$]*$/.test(String(key || ''))
		? key
		: JSON.stringify(key);
}

function serializeTsValue(value, indentLevel = 0) {
	const indent = '\t'.repeat(indentLevel);
	const nextIndent = '\t'.repeat(indentLevel + 1);

	if (Array.isArray(value)) {
		if (!value.length) {
			return '[]';
		}

		return `[\n${value
			.map(item => `${nextIndent}${serializeTsValue(item, indentLevel + 1)}`)
			.join(',\n')}\n${indent}]`;
	}

	if (value && typeof value === 'object') {
		const entries = Object.entries(value);
		if (!entries.length) {
			return '{}';
		}

		return `{\n${entries
			.map(
				([key, item]) =>
					`${nextIndent}${formatTsObjectKey(key)}: ${serializeTsValue(item, indentLevel + 1)}`
			)
			.join(',\n')}\n${indent}}`;
	}

	return JSON.stringify(value);
}

function buildPermissionsGeneratedSource(model) {
	const lines = [
		'/**',
		' * Global admin permissions generated from base menu.json.',
		' * Do not edit this file manually.',
		` * Source: ${path.relative(REPO_ROOT, MENU_SOURCE_PATH)}`,
		' */',
		'',
		`export const PERMISSION_SOURCE_FILE = ${JSON.stringify(
			BASE_PERMISSION_SOURCE_CONFIG.menuSourcePath
		)};`,
		'',
		`export const ADMIN_PERMISSION_KEYS = ${serializeTsValue(model.permissionKeys)} as const;`,
		'',
		'export type AdminPermissionKey = (typeof ADMIN_PERMISSION_KEYS)[number];',
		'',
		`export const PERMISSIONS = ${serializeTsValue(model.permissionTree)} as const;`,
		''
	];

	return `${lines.join('\n')}\n`;
}

function buildPermissionBitsGeneratedSource(model) {
	const lines = [
		'/**',
		' * Global admin permission bits generated from base menu.json.',
		' * Do not edit this file manually.',
		` * Source: ${path.relative(REPO_ROOT, MENU_SOURCE_PATH)}`,
		' */',
		'',
		"import type { AdminPermissionKey } from './permissions.generated';",
		'',
		'const ZERO_PERMISSION_MASK = BigInt(0);',
		'const ONE_PERMISSION_MASK = BigInt(1);',
		'',
		'function permissionBit(shift: number) {',
		'\treturn ONE_PERMISSION_MASK << BigInt(shift);',
		'}',
		'',
		'export const SUPER_ADMIN_PERMISSION_BIT = permissionBit(0);',
		'',
		'export const PERMISSION_BIT_BY_KEY: Record<AdminPermissionKey, bigint> = {'
	];

	model.permissionKeys.forEach((permissionKey, index) => {
		lines.push(`\t${JSON.stringify(permissionKey)}: permissionBit(${index + 1}),`);
	});

	lines.push('};', '');
	lines.push(
		`export const WRITE_PERMISSION_KEYS = ${serializeTsValue(model.writePermissionKeys)} as const;`,
		'',
		'export const WRITE_PERMISSION_BITS = WRITE_PERMISSION_KEYS.map(',
		'\tpermissionKey => PERMISSION_BIT_BY_KEY[permissionKey]',
		');',
		'',
		'export const ALL_PERMISSION_BITS = Object.values(PERMISSION_BIT_BY_KEY);',
		'',
		'export function normalizePermissionKey(value: unknown): AdminPermissionKey | null {',
		"\tconst normalized = String(value || '').trim();",
		'\treturn normalized in PERMISSION_BIT_BY_KEY',
		'\t\t? (normalized as AdminPermissionKey)',
		'\t\t: null;',
		'}',
		'',
		'export function parsePermissionMask(mask: unknown) {',
		"\tif (typeof mask === 'bigint') {",
		'\t\treturn mask;',
		'\t}',
		"\tif (typeof mask === 'number' && Number.isFinite(mask)) {",
		'\t\treturn BigInt(Math.trunc(mask));',
		'\t}',
		"\tif (typeof mask === 'string' && mask.trim()) {",
		'\t\ttry {',
		'\t\t\treturn BigInt(mask.trim());',
		'\t\t} catch (error) {',
		'\t\t\treturn ZERO_PERMISSION_MASK;',
		'\t\t}',
		'\t}',
		'\treturn ZERO_PERMISSION_MASK;',
		'}',
		'',
		'export function formatPermissionMask(mask: bigint) {',
		'\treturn mask.toString();',
		'}',
		'',
		'export function resolvePermissionMask(',
		'\tperms: readonly string[] = [],',
		'\toptions?: { isAdmin?: boolean }',
		') {',
		'\tlet mask = ZERO_PERMISSION_MASK;',
		'\tif (options?.isAdmin) {',
		'\t\tmask |= SUPER_ADMIN_PERMISSION_BIT;',
		'\t}',
		'\tfor (const permissionKey of perms) {',
		'\t\tconst normalizedPermissionKey = normalizePermissionKey(permissionKey);',
		'\t\tif (normalizedPermissionKey) {',
		'\t\t\tmask |= PERMISSION_BIT_BY_KEY[normalizedPermissionKey];',
		'\t\t}',
		'\t}',
		'\treturn formatPermissionMask(mask);',
		'}',
		'',
		'export function hasPermissionBit(mask: unknown, required: unknown) {',
		'\tconst parsedMask = parsePermissionMask(mask);',
		'\tconst parsedRequired = parsePermissionMask(required);',
		'\tif (parsedRequired === ZERO_PERMISSION_MASK) {',
		'\t\treturn true;',
		'\t}',
		'\treturn (',
		'\t\t(parsedMask & SUPER_ADMIN_PERMISSION_BIT) === SUPER_ADMIN_PERMISSION_BIT ||',
		'\t\t(parsedMask & parsedRequired) === parsedRequired',
		'\t);',
		'}',
		'',
		'export function hasAnyPermissionBit(mask: unknown, requiredBits: Iterable<unknown>) {',
		'\tconst parsedMask = parsePermissionMask(mask);',
		'\tif ((parsedMask & SUPER_ADMIN_PERMISSION_BIT) === SUPER_ADMIN_PERMISSION_BIT) {',
		'\t\treturn true;',
		'\t}',
		'\tfor (const requiredBit of requiredBits) {',
		'\t\tconst parsedRequired = parsePermissionMask(requiredBit);',
		'\t\tif (',
		'\t\t\tparsedRequired !== ZERO_PERMISSION_MASK &&',
		'\t\t\t(parsedMask & parsedRequired) === parsedRequired',
		'\t\t) {',
		'\t\t\treturn true;',
		'\t\t}',
		'\t}',
		'\treturn false;',
		'}',
		''
	);

	return `${lines.join('\n')}\n`;
}

function buildRoutePermissionsGeneratedSource(model) {
	const routePermissionMap = {};

	for (const item of model.routeEntries) {
		routePermissionMap[item.path] = item.permission;
	}

	const lines = [
		'/**',
		' * Global admin route permission map generated from base menu.json.',
		' * Do not edit this file manually.',
		` * Source: ${path.relative(REPO_ROOT, MENU_SOURCE_PATH)}`,
		' */',
		'',
		`export const ROUTE_PERMISSION_BY_PATH = ${serializeTsValue(routePermissionMap)} as const;`,
		''
	];

	return `${lines.join('\n')}\n`;
}

function buildGeneratedSources(model) {
	return {
		'permissions.generated.ts': buildPermissionsGeneratedSource(model),
		'permission-bits.generated.ts': buildPermissionBitsGeneratedSource(model),
		'route-permissions.generated.ts': buildRoutePermissionsGeneratedSource(model)
	};
}

function writeGeneratedFiles(targetDirectory, files, checkOnly) {
	const changedFiles = [];

	fs.mkdirSync(targetDirectory, { recursive: true });

	for (const [fileName, content] of Object.entries(files)) {
		const outputPath = path.join(targetDirectory, fileName);
		const existingContent = fs.existsSync(outputPath)
			? fs.readFileSync(outputPath, 'utf8')
			: null;

		if (existingContent === content) {
			continue;
		}

		changedFiles.push(outputPath);

		if (!checkOnly) {
			fs.writeFileSync(outputPath, content, 'utf8');
		}
	}

	return changedFiles;
}

export function generatePermissions(options = {}) {
	const checkOnly = options.check === true;
	const menuTree = JSON.parse(fs.readFileSync(MENU_SOURCE_PATH, 'utf8'));
	const model = collectPermissionModel(menuTree, {
		writeActions: BASE_PERMISSION_SOURCE_CONFIG.writeActions,
		routePermissionPriority:
			BASE_PERMISSION_SOURCE_CONFIG.routePermissionPriority
	});
	const generatedSources = buildGeneratedSources(model);
	const changedFiles = [];

	for (const targetDirectory of OUTPUT_TARGETS) {
		changedFiles.push(
			...writeGeneratedFiles(targetDirectory, generatedSources, checkOnly)
		);
	}

	return {
		checkOnly,
		model,
		changedFiles
	};
}

function main() {
	const checkOnly = process.argv.includes('--check');
	const result = generatePermissions({ check: checkOnly });

	if (checkOnly) {
		if (result.changedFiles.length) {
			console.error('Permission artifacts are out of date:');
			for (const changedFile of result.changedFiles) {
				console.error(`- ${path.relative(REPO_ROOT, changedFile)}`);
			}
			process.exitCode = 1;
			return;
		}

		console.log('Permission artifacts are up to date.');
		return;
	}

	console.log(
		`Generated global admin permissions: ${result.model.permissionKeys.length} keys, ${result.model.routeEntries.length} routes.`
	);
	for (const targetDirectory of OUTPUT_TARGETS) {
		console.log(`- ${path.relative(REPO_ROOT, targetDirectory)}`);
	}
}

const isDirectExecution =
	process.argv[1] &&
	path.resolve(process.argv[1]) === fileURLToPath(import.meta.url);

if (isDirectExecution) {
	main();
}
